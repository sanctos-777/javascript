// adição: (+)
// subtração: (-)
// divisão: (/)
// mulriplicação:(*)
// resto da divisão:(%)
// incremento: (++)
// descremento: (--)
// expoente: (**)

// operador aritmetico soma
let v1 = 2 
let v2 = 3
let soma = v1+v2
console.log("a soma é:"+soma)
console.log("a soma é:",v1+v2) //conversão
console.log("a soma é:"+Number(v1+v2)) //conversão

// operador aritmetico subtração
let v3 = 10 
let v4 = 5
let subtração = v3-v4
console.log("a subtração é:"+subtração)
console.log("a subtração é:",v3-v4) //conversão
console.log("a subtração é:"+Number(v3-v4)) //conversão

// operador aritmetico divisão
let v5 = 12
let v6 = 2
let divisão = v5/v6
console.log("a divisão é:"+divisão)
console.log("a divisão é:",v5/v6) //conversão
console.log("a divisão é:"+Number(v5/v6)) //conversão

// operador aritmetico mult
let v7 = 12
let v8 = 2
let mult = v7*v8
console.log("a mult é:"+mult)
console.log("a mult é:",v7*v8) //conversão
console.log("a mult é:"+Number(v7*v8)) //conversão